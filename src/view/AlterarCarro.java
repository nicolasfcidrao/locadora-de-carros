    public AlterarCarro() {
        tabelaModelo.addColumn("Placa");
        tabelaModelo.addColumn("Modelo");
        tabelaModelo.addColumn("Cor");
        tabelaModelo.addColumn("Disponibilidade");

        CarroMysqlDAO cDao = new CarroMysqlDAO();
        List<Carro> carros = cDao.listar();

        for (Carro car : carros) {
            tabelaModelo.addRow(new Object[]{car.getPlaca(), car.getModelo(), car.getCor(), car.getDisponibilidade()});
        }
        initComponents();
    }

