    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        AlterarCliente aC = new AlterarCliente();

        UsuarioMysqlDAO uDao = new UsuarioMysqlDAO();
        String nome = tfNome.getText();
        String cpf = tfCPF.getText();
        String email = tfEmail.getText();
        String senha = tfSenha.getText();
        int idade = Integer.parseInt(tfIdade.getText());
        long telefone = Long.parseLong(tfTelef.getText());
        boolean habilitado = comboHabilit.getModel().getSelectedItem().equals("Sim");

        Usuario u = new Usuario(nome, cpf, email, senha, idade, telefone, habilitado);
        uDao.alterar(u);

        JOptionPane.showMessageDialog(null, "Cliente alterado!");

        this.setVisible(false);
        LoginFuncionario lF = new LoginFuncionario();
        lF.setVisible(true);
    }//GEN-LAST:event_btnAlterarActionPerformed

